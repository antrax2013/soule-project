<h1>Présentation du jeu Soule Royale</h1>
<h2>Naissance de la soule royale</h2>
<p>La Soule Royale est un jeu qui à vu le jour grâce au jeu les Royaumes Renaissants. Les royaumes renaissants est un jeu de gestion où des villages de joueurs sont en interdépendance. Les rivalités exacerbées ont donné naissance à des rencontres de soules. Petit à petit des équipes ont été crées dans les villages. Puis des championnats ont vu le jour, des coupes ... La soule royale est née. Quand, c'est difficile de dire mais il est certain qu'en 2005 elle était déjà bien vivante. Antrax, capitaine des brisegnolais, remportait la premier coupe du Saint Empire Germanique.</p>
<h2>Le jeu</h2>
<p>La Soule Royale se joue par équipe de 11 souleurs menée par un capitaine. L'objectif est de déposé la soule, sorte de balle, dans la zone d'embut de l'équipe adverse.</p> 
<p>Les matchs se déroulent sur 7 tours. Chacuns des joueurs disposent de 4 points de vie au début du match. Ils agissent dans l'ordre des numéros alternativement par équipe. C'est à dire que le joueur avec le numéro 1, par exemple, de l'équipe 1 joue, puis le joueur numéro 1 de l'équipe 2. Ensuite c'est au joueur numéro 2 de l'équipe 1 et ainsi de suite.</p>
<p>Les joueurs peuvent avancer, reculer, frapper un autre joueur ou ne rien faire. La première équipe qui marque remporte la rencontre.</p>
