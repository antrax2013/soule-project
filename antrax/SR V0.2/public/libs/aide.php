                                <h2>4- Recevoir des photos par email.</h2>
                                <p>
                                Pour recevoir des photos par email. Il faut au préalable sélectionner les photos qui vous intéressent.
                                <br />
                                Pour cela choisissez un album. Ensuite, faites apparaître dans le cadre central la photo qui vous plait. Une fois celle dans le cadre cliquez sur <img src="images/panier1.png" class="icone"> pour la mémoriser.
                                <br />Vous pouvez mémoriser autant de photos que vous le souhaitez même si elles sont issues d'albums différents. Pour faciliter la réception, celles-ci seront envoyées dans un fichier zip par tranche de 10Mo. Le delais d'envoi est variable en fonction des demandes.
                                <br />Pour les recevoir par email, cliquer sur <img src="images/send_img1.png" class="icone"> et remplissez le formulaire. Si toutefois, une photo ne vous convient plus, au moment du remplissage du formulaire, il suffit de décocher la case se trouvant à gauche de la vignette et elle ne vous sera pas envoyée.
                                <br /><span class="warning">Attention, si vous quittez le site sans avoir rempli le formulaire d'envoi, votre sélection sera définitivement perdue.</span>
