<h1>Présentation générale de la soule</h1>
<h2>La soule c'est quoi ?</h2>
<p>La <strong>soule</strong> est un jeu traditionnel pratiqué sous l'ancien régime principalement en France. Ancêtre présumé du football et du rugby, la <strong>soule</strong> se rapproche de ce dernier, si ce n'est que les équipes lors des rencontres de <strong>soule</strong> ont souvent un même en but: un lieu dit, une mare... La composition des équipes de <strong>soule</strong> n'a pas de limites définies. A l'origine les équipes étaient constituées de tous les hommes valides de deux ou plusieurs villages. A la <strong>soule</strong> tous les coups sont permis. Ce jeu est le sujet d'un film homonyme, La <strong>Soule</strong>, de Michel Sibra (1989), avec Richard Bohringer et Christophe Malavoy.</p>
<p>La <strong>soule</strong> se prononçant choule en Normandie et en Picardie. En Normandie, on pratique la <strong>soule</strong> sur la base de règles modernisées alors qu'en Picardie la pratique de la <strong>soule</strong> est plus traditionnelle.</p>
<p>La <strong>soule</strong> est de nouveau pratiquée très régulièrement dans ces deux régions, y compris avec des crosses surtout en Normandie, où les derniers témoignages antérieurs à une résurrection depuis 2001, remontaient à la Seconde Guerre mondiale dans le Cotentin.</p>
<p>Des liens importants existent avec des sports anciens:</p>
<ul>
<li>L'haspartum partiqué par les romains puis Calcio florentin en Italie du Quattrocento</li>
<li>Dans les pays scandinaves, sous le nom de knattleikr</li>
<li>Dans les pays anglo-saxons avec le hurling et le football gaélique irlandais.</li>
</ul> 
<p>La <strong>soule</strong> vit une renaissance depuis le début du XXe avec le shinty écossais, mais aussi avec des sports plus modernes ou moins traditionnels:</p>
<ul>
<li>les footballs américains et australiens</li> 
<li>la crosse canadienne, jouée à l'origine par les Amérindiens mixant sans doute leur jeu avec celui des colons</li>
<li>un jeu traditionnel avec les crosses au Chili</li>
</ul>
<p>Ces sports ou jeux, apparenté à <strong>soule</strong> sont essentiellement joués lors des fêtes des saints patrons et à l'époque de carnaval.</p>
